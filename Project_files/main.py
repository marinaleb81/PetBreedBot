# Загрузка модели
model = timm.create_model('xception', pretrained=False, num_classes=len(class_names))
model.fc = torch.nn.Linear(model.fc.in_features, len(class_names))
model.load_state_dict(torch.load("/app/models/xception_132.pth", map_location=torch.device('cpu')))
model.eval()

# Преобразования для изображений
preprocess = transforms.Compose([
    transforms.Resize((299, 299)),
    transforms.ToTensor(),
    transforms.Normalize(mean=[0.485, 0.456, 0.406], std=[0.229, 0.224, 0.225]),
])

@app.post("/predict")
async def predict(file: UploadFile):
    image = Image.open(io.BytesIO(await file.read())).convert('RGB')
    input_tensor = preprocess(image).unsqueeze(0)
    with torch.no_grad():
        output = model(input_tensor)
        probabilities = torch.nn.functional.softmax(output[0], dim=0)
        predicted_idx = torch.argmax(probabilities).item()
        predicted_prob = probabilities[predicted_idx].item()
    return {"breed": class_names[predicted_idx], "probability": predicted_prob}
